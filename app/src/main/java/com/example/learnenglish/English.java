package com.example.learnenglish;

public class English {

    private String mAlphabut;
    private String mWord;
    private int mImage;

    public English(String Alphabut, String Word, int Image)
    {
        mAlphabut=Alphabut;
        mWord=Word;
        mImage=Image;
    }

    public String getmAlphabut()
    {
        return mAlphabut;
    }

    public String getmWord()
    {
        return mWord;
    }

    public int getmImage()
    {
        return mImage;
    }

}
